package BEAN;

public class provaBEAN {
    private float   id;
    private float   prova_parcial;
    private float   prova_bimestral;
    private float   media;
    private String  resultado;

    public float getId() {
        return id;
    }

    public void setId(float id) {
        this.id = id;
    }

    public float getProva_parcial() {
        return prova_parcial;
    }

    public void setProva_parcial(float prova_parcial) {
        this.prova_parcial = prova_parcial;
    }

    public float getProva_bimestral() {
        return prova_bimestral;
    }

    public void setProva_bimestral(float prova_bimestral) {
        this.prova_bimestral = prova_bimestral;
    }

    public float getMedia() {
        return media;
    }

    public void setMedia(float media) {
        this.media = media;
    }

    public String getResultado() {
        return resultado;
    }

    public void setResultado(String resultado) {
        this.resultado = resultado;
    }
    
    
}
